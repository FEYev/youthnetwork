function applyLanguage(lang) {
  const selected = translations[lang] || translations.no;
  document.documentElement.lang = lang === "en" ? "en" : "no";
  document.body.dataset.lang = lang;
  localStorage.setItem("youthNetworkLanguage", lang);

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.getAttribute("data-i18n");
    if (selected[key]) {
      element.textContent = selected[key];
    }
  });

  const titleKey = document.body.getAttribute("data-title-key");
  if (titleKey && selected[titleKey]) {
    document.title = selected[titleKey];
  }

  document.querySelectorAll("[data-lang-button]").forEach((button) => {
    const isActive = button.getAttribute("data-lang-button") === lang;
    button.classList.toggle("active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });
}

function setupNavigation() {
  const toggle = document.querySelector(".nav-toggle");
  const nav = document.querySelector(".site-nav");
  if (!toggle || !nav) return;

  toggle.addEventListener("click", () => {
    const isOpen = nav.classList.toggle("open");
    toggle.setAttribute("aria-expanded", String(isOpen));
  });

  nav.querySelectorAll("a").forEach((link) => {
    link.addEventListener("click", () => {
      nav.classList.remove("open");
      toggle.setAttribute("aria-expanded", "false");
    });
  });
}

function setupLanguageButtons() {
  document.querySelectorAll("[data-lang-button]").forEach((button) => {
    button.addEventListener("click", () => {
      applyLanguage(button.getAttribute("data-lang-button"));
    });
  });
}

function setCurrentYear() {
  const year = document.getElementById("current-year");
  if (year) year.textContent = new Date().getFullYear();
}

document.addEventListener("DOMContentLoaded", () => {
  setupNavigation();
  setupLanguageButtons();
  setCurrentYear();
  const savedLanguage = localStorage.getItem("youthNetworkLanguage") || "no";
  applyLanguage(savedLanguage);
});
